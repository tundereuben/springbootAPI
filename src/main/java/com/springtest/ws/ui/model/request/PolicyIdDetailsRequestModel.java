package com.springtest.ws.ui.model.request;

public class PolicyIdDetailsRequestModel {

    private int idCode;
    private String idName;
    private String idType;
    private int idPolCode;
    private int idSclCode;

    public int getIdCode() {
        return idCode;
    }

    public void setIdCode(int idCode) {
        this.idCode = idCode;
    }

    public String getIdName() {
        return idName;
    }

    public void setIdName(String idName) {
        this.idName = idName;
    }

    public String getIdType() {
        return idType;
    }

    public void setIdType(String idType) {
        this.idType = idType;
    }

    public int getIdPolCode() {
        return idPolCode;
    }

    public void setIdPolCode(int idPolCode) {
        this.idPolCode = idPolCode;
    }

    public int getIdSclCode() {
        return idSclCode;
    }

    public void setIdSclCode(int idSclCode) {
        this.idSclCode = idSclCode;
    }
}
